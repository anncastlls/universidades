    <h2>By Mora, YovngChimi </h2>
    <h3>"Modelito", del artista puertorriqueño Mora junto a YovngChimi, forma parte del álbum Paraíso (2022/2023). La canción es un reguetón fiestero que narra una noche de fiesta de barrio en San Juan, Puerto Rico, enfocándose en la sensualidad, el baile y la atracción física en un ambiente nocturnos y urbano, grabado en las calles de la capital</h2>
